% Define two numbers
a = 5;
b = 3;

% 1. Addition
addition_result = a + b;
fprintf('Addition: %.2f + %.2f = %.2f\n', a, b, addition_result);

% 2. Multiplication
multiplication_result = a * b;
fprintf('Multiplication: %.2f * %.2f = %.2f\n', a, b, multiplication_result);

% 3. Division
division_result = a / b;
fprintf('Division: %.2f / %.2f = %.2f\n', a, b, division_result);

% 4. Exponentiation
exponentiation_result = a^b; % a raised to the power of b
fprintf('Exponentiation: %.2f ^ %.2f = %.2f\n', a, b, exponentiation_result);
